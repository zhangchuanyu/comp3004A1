package core;

public class ArithmeticSolver {
	public double add(double leftoper , double rightoper) {
		return leftoper+rightoper;
	}
	public double subtract(double leftoper , double rightoper) {
		return leftoper-rightoper;
	}
	public double multiply(double leftoper , double rightoper) {
		return leftoper*rightoper;
	}
	public double divide(double leftoper , double rightoper) {
		return leftoper/rightoper;
	}
	public double modulus(double leftoper , double rightoper) {
		return leftoper%rightoper;
	}
}
